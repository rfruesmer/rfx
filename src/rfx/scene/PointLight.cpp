#include "rfx/pch.h"
#include "rfx/scene/PointLight.h"

using namespace rfx;
using namespace glm;
using namespace std;


// ---------------------------------------------------------------------------------------------------------------------

void PointLight::setPosition(const vec3& position)
{
    position_ = position;
}

// ---------------------------------------------------------------------------------------------------------------------

const vec3& PointLight::getPosition() const
{
    return position_;
}

// ---------------------------------------------------------------------------------------------------------------------

void PointLight::setRange(float range)
{
    range_ = range;
}

// ---------------------------------------------------------------------------------------------------------------------

float PointLight::getRange() const
{
    return range_;
}

