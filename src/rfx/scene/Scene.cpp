#include "rfx/pch.h"
#include "rfx/scene/Scene.h"


using namespace rfx;
using namespace std;


// ---------------------------------------------------------------------------------------------------------------------

Scene::Scene()
    : rootNode(make_unique<SceneNode>()) {}

// ---------------------------------------------------------------------------------------------------------------------

void Scene::setCamera(const std::shared_ptr<Camera>& camera)
{
    this->camera = camera;
}

// ---------------------------------------------------------------------------------------------------------------------

const shared_ptr<Camera>& Scene::getCamera() const
{
    return camera;
}

// ---------------------------------------------------------------------------------------------------------------------

const unique_ptr<SceneNode>& Scene::getRootNode() const
{
    return rootNode;
}

// ---------------------------------------------------------------------------------------------------------------------

void Scene::add(unique_ptr<SceneNode>& node) const
{
    rootNode->attach(node);
}

// ---------------------------------------------------------------------------------------------------------------------

void Scene::add(const shared_ptr<Light>& light)
{
    lights.push_back(light);
}

// ---------------------------------------------------------------------------------------------------------------------

const vector<shared_ptr<Light>>& Scene::getLights() const
{
    return lights;
}

