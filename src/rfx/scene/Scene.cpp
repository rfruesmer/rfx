#include "rfx/pch.h"
#include "rfx/scene/Scene.h"


using namespace rfx;
using namespace std;


// ---------------------------------------------------------------------------------------------------------------------

Scene::Scene()
    : rootNode(make_unique<SceneNode>()) {}

// ---------------------------------------------------------------------------------------------------------------------

void Scene::add(unique_ptr<SceneNode>& node) const
{
    rootNode->attach(node);
}

// ---------------------------------------------------------------------------------------------------------------------

const unique_ptr<SceneNode>& Scene::getRootNode() const
{
    return rootNode;
}

